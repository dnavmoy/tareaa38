    private void mostrarDatosEditar() {
        // Obtengo el id de la persona seleccionada
        // Para ello, obtengo la fila seleccionada y luego el id de esa fila
        int fila = filaSeleccionadaJTable(padre.getJTable());
        // El id de la persona es el valor de la columna cero de esa fila
        int idDonante = (int)padre.getJTable().getValueAt(fila, 0);
        // Guarda la persona seleccionada
        this.donante = padre.getListaDonantes().getDonante(idDonante);
        // Muestra datos de la persona que se seleccion√≥ en el jtable
        // en los jtextfield
        jTextField1.setText(String.valueOf(donante.getIdPaciente()));
        jTextField1.setEditable(false); // No editable
        jTextField1.setBackground(Color.GRAY);
        jTextField2.setText(this.donante.getNombre());
        jTextField3.setText(this.donante.getFechaNacimiento());
        jTextField4.setText(this.donante.getGrupoSanguineo());
        jTextField5.setText(this.donante.getRh());
        jTextField6.setText(String.valueOf(this.donante.getNumeroDonaciones()));
        
    }
    
      private int filaSeleccionadaJTable(JTable jTable1) {
        int fila = jTable1.getSelectedRow();
        return fila;
    }
    
    
